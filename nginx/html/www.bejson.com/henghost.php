							if (chidItem.id === item.elasticityid) {
								addPrice += Number(chidItem.oprice[item.default_cycle])
							}
						})
					}
					totalPrice = Number(basicPrice) + Number(addPrice)

					return totalPrice * Number(this.productDetailInfo.num)
