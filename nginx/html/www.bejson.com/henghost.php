					}


					if (item.ipsId !== undefined) {
						if (item.ipsSelect && item.ipsSelect.length > 0) {
							item.ipsSelect.forEach(function (childItem) {
								if (childItem.ipsId === item.ipsId) {
									addPrice += Number(childItem.oprice[item.default_cycle])
								}
							})
						}
					}

					if (item.ramSelect && item.ramSelect.length) {
						item.ramSelect.forEach(function (chidItem) {
							if (chidItem.ramid === item.ramid) {
								addPrice += Number(chidItem.oprice[item.default_cycle])
							}
						})
					}

					if (item.fySelect && item.fySelect.length) {
